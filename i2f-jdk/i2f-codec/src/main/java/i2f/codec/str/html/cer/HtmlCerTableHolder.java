    public static AtomicBoolean INITIAL = new AtomicBoolean(false);
    public static ConcurrentHashMap<String, Integer> cer2CharMap = new ConcurrentHashMap<>();
    public static ConcurrentHashMap<Integer, String> char2CerMap = new ConcurrentHashMap<>();

    public static String encode(int ch) {
        if (ch < 0) {
            return "";
        }
        if (!INITIAL.get()) {
            init();
        }
        String str = char2CerMap.get(ch);
        if (str == null) {
            return String.valueOf((char) ch);
        }
        return str;
    }

    public static int decode(String str) {
        if (str == null) {
            return -1;
        }
        if (!INITIAL.get()) {
            init();
        }
        Integer val = cer2CharMap.get(str.toLowerCase());
        if (val == null) {
            return -1;
        }
        return (int) val;
    }

    public static synchronized void init() {
        cer2CharMap.clear();
        char2CerMap.clear();
        for (String[] line : FULL_CER_TABLE) {
            String cer = line[0].toLowerCase();
            if (!cer.startsWith("&")) {
                cer = "&" + cer;
            }
            if (!cer.endsWith(";")) {
                cer = cer + ";";
            }
            int unicode = Integer.parseInt(line[1].substring(2), 16);
            cer2CharMap.put(cer, unicode);
            char2CerMap.put(unicode, cer);
        }

        INITIAL.set(true);
    }

