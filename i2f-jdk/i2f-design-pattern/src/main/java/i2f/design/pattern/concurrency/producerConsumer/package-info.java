/**
 * 生产者-消费者模式（Producer-Consumer）
 * <p>
 * 通过共享缓冲区解耦生产者与消费者，生产者向缓冲区放入数据，消费者从缓冲区取出数据。
 * 分类：并发型模式
 * 
 * <p><b>场景说明：</b>以"餐厅厨房"为场景——厨师（生产者）制作菜品放入出餐台（固定大小缓冲区），
 * 服务员（消费者）从出餐台取走菜品送给顾客。当出餐台满时厨师需要等待，
 * 当出餐台空时服务员需要等待，通过这种机制实现生产与消费的协调。</p>
 * 
 * <p><b>核心机制：</b></p>
 * <ul>
 *   <li>固定容量缓冲区：限制同时存在的菜品数量，防止资源耗尽</li>
 *   <li>生产者阻塞：缓冲区满时，生产者等待消费者取出菜品后再继续制作</li>
 *   <li>消费者阻塞：缓冲区空时，消费者等待厨师制作新菜品后再继续取餐</li>
 *   <li>线程安全：使用 synchronized 和 wait/notify 机制保证并发安全</li>
 * </ul>
 * 
 * <p><b>模式结构：</b></p>
 * <pre>
 *  Producer（生产者）          SharedBuffer（共享缓冲区）      Consumer（消费者）
 *  ─────────────────────────   ─────────────────────────────   ─────────────────────────
 *  Chef（厨师）                 FixedSizeBuffer（固定容量）      Waiter（服务员）
 *    ├─ produce(dish)            ├─ put(dish)                     ├─ consume()
 *    └─ 制作菜品                   ├─ take()                        └─ 送餐给顾客
 *                                  └─ 容量控制/阻塞协调
 * </pre>
 * </p>
 */
package i2f.design.pattern.concurrency.producerConsumer;
